echo "Creating a new file named 'example.txt'..."
touch example.txt
echo "File 'example.txt' created successfully!"

echo "Writing text to 'example.txt'..."
echo "This is a sample file for demonstrating file commands." > example.txt
echo "Text written to 'example.txt'."

# Display the contents of the file
echo "Displaying the contents of 'example.txt':"
cat example.txt

echo "Creating a copy of 'example.txt' named 'example_copy.txt'..."
cp example.txt example_copy.txt
echo "File copied successfully!"

